print("|---|---|---|---|---|---|")
for idx, (start, end, term, entry, host) in enumerate(rows, start=1):
    disp_start = normalize(start)
    disp_end = normalize(end)
    if abs(disp_end - 360.0) < 1e-9:
        disp_end = 360.0
    start_str = f"{disp_start:.3f}°"
    end_str = f"{disp_end:.3f}°"
    print(f"| {idx} | {start_str} | {end_str} | {term} | {entry} | {host} |")

