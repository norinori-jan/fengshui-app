sector = 360.0 / 64.0
anchor_index = 13
anchor_start = 15.0
base_start = anchor_start - anchor_index * sector


def normalize(angle):
    a = angle % 360.0
    return 360.0 if abs(a - 0.0) < 1e-9 and angle != 0.0 else a


def find_term(angle):
    a = angle % 360.0
    for start, name in reversed(terms):
        if a >= start:
            return name
    return terms[-1][1]


def find_host(angle):
    a = angle % 360.0
    for start, end, name in hosts:
        if start <= a < end:
            return name
    if abs(a - 360.0) < 1e-9 or abs(a) < 1e-9:
        return "女"
    return "不明"


def find_entry(angle):
    idx = int(math.floor((angle - base_start) / sector)) % 64
    return entries[idx]


breakpoints = set()

# 節気境界 (300-660)
for i in range(25):
    breakpoints.add(300.0 + i * 15.0)

# 第1-4層境界 (原点ベース)
for i in range(65):
    breakpoints.add(base_start + i * sector)

# 宿地点境界（0-360と360-720）
for start, end, _ in hosts:
    breakpoints.add(start)
    breakpoints.add(end)
    breakpoints.add(start + 360.0)
    breakpoints.add(end + 360.0)

# 300から660の範囲内で余分な境界と重複を整理
bounds = sorted([b for b in breakpoints if 300.0 <= b <= 660.0])

rows = []
for i in range(len(bounds) - 1):
    start = bounds[i]
    end = bounds[i + 1]
    if end - start < 1e-9:
        continue
    term = find_term(start)
    l1, l2, l3, l4 = find_entry(start)
    host = find_host(start)
    rows.append((start, end, term, f"{l1}/{l2}/{l3}/{l4}", host))

