def analyze_lopan(degree):
    """
    入力された角度から、第1層〜第8層までの情報を算出するエンジン
    """
    # 338.0度を起点(0)として正規化
    start_offset = 338.0
    normalized_d = (degree - start_offset) % 360
    
    # 第1層：卦 (1卦 45度)
    gua_idx = int(normalized_d // 45)
    gua_data = LOPAN_MASTER_DATA[gua_idx]
    
    # 15度スロット (第2〜6層の共通単位)
    d_within_gua = normalized_d % 45
    slot_idx = int(d_within_gua // 15)
    slot_data = gua_data["slots"][slot_idx]
    
    # 第7層：分金 (15度を3分割 = 1つ5度)
    d_within_slot = d_within_gua % 15
    l7_idx = int(d_within_slot // 5)
    l7_name = slot_data["l7"][l7_idx]

    # 第8層：(15度を5分割 = 1つ3度)
    l8_idx = int(d_within_slot // 3)
    l8_name = slot_data["l8"][l8_idx]

    return {
        "角度": f"{degree}°",
        "L1_卦": f"{gua_data['symbol']} ({gua_data['gua']})",
        "L2_二十四山": slot_data["l2"],
        "L3_詳細格": slot_data["l3"],
        "L4_天星": slot_data["l4"],
        "L5_九星": slot_data["l5"],
        "L6_地盤五行": f"{slot_data['l6']}({slot_data['element']})",
        "L7_分金": l7_name,
        "L8_透地": l8_name,
        "重要度": "【★重要：赤抜き★】" if slot_data["is_important"] else "通常"
    }

